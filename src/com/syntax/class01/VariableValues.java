package com.syntax.class01;

public class VariableValues {

	public static void main(String[] args) {
		//storing and representing whole numbers values;
		
		byte storage1=99;
		short storage2=999;
		int storage3=100000;
		long storage4=382754854298l;
		
		//storing and representing decimal values;
		
		float roomtemp1=50.00f;
		double roomtemp2=99.5;
		
		//storing and representing  single characters;
		
		char classroom='A';
		char bus='$';
		
		//storing yes or no values (true or false);
		
		boolean cloudyday=false;
		boolean sunnyday=true;
		
		System.out.println (storage1);
		System.out.println (storage2);
		System.out.println (storage3);
		System.out.println (storage4);
		System.out.println (roomtemp1);
		System.out.println (roomtemp2);
		System.out.println (classroom);
		System.out.println (bus);
		System.out.println (cloudyday);
		System.out.println (sunnyday);
		
		
		
				
		
		
		
		
		
	}

}
